void ov12870_init(VI_PIPE ViPipe)
{
    HI_U32          i;
    HI_U8            u8ImgMode;
    HI_BOOL         bInit;

    bInit       = g_pastOv12870[ViPipe]->bInit;
    u8ImgMode   = g_pastOv12870[ViPipe]->u8ImgMode;
    HI_U8 u16RegData;
    HI_U16 u16RegAddr;
    HI_U32 u32SeqEntries;

    if (bInit == HI_FALSE) {
        /* 2. sensor i2c init */
        ov12870_i2c_init(ViPipe);
    }

    if ((!g_bStandby[ViPipe]) || (!g_pastOv12870[ViPipe]->bInit)) {

        u32SeqEntries = sizeof(gs_au16SensorCfgSeqComm) / sizeof(gs_au16SensorCfgSeqComm[0]);

        for ( i = 0; i < u32SeqEntries; i++ ) {
            u16RegAddr = gs_au16SensorCfgSeqComm[i][1];
            u16RegData = gs_au16SensorCfgSeqComm[i][0];

            if (u16RegData == NA) {
                continue;
            }

            ov12870_write_register(ViPipe, u16RegAddr, u16RegData);
        }
    }

    u32SeqEntries = sizeof(gs_au16SensorCfgSeq) / sizeof(gs_au16SensorCfgSeq[0]);

    for ( i = 0; i < u32SeqEntries; i++ ) {
        u16RegAddr = gs_au16SensorCfgSeq[i][OV12870_MODE_BUTT];
        u16RegData = gs_au16SensorCfgSeq[i][u8ImgMode];

        if (u16RegData == NA) {
            continue;
        }

        ov12870_write_register(ViPipe, u16RegAddr, u16RegData);
    }

    for (i = 0; i < g_pastOv12870[ViPipe]->astRegsInfo[0].u32RegNum; i++) {
        ov12870_write_register(ViPipe, g_pastOv12870[ViPipe]->astRegsInfo[0].astI2cData[i].u32RegAddr, g_pastOv12870[ViPipe]->astRegsInfo[0].astI2cData[i].u32Data);
    }

    g_pastOv12870[ViPipe]->bInit = HI_TRUE;
    ov12870_restart(ViPipe);

    printf("OV12870 %s init succuss!\n", g_astOv12870ModeTbl[u8ImgMode].pszModeName);

    return;

}

void ov12870_exit(VI_PIPE ViPipe)
{
    ov12870_i2c_exit(ViPipe);
    g_bStandby[ViPipe] = HI_FALSE;

    return;
}
