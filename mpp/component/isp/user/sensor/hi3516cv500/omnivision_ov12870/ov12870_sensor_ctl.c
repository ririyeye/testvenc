int ov12870_write_register(VI_PIPE ViPipe, int addr, int data)
{
    int ret;
    int idx = 0;
    char buf[8];

    if (g_fd[ViPipe] < 0) {
        return HI_SUCCESS;
    }

    if (ov12870_addr_byte == 2) {
        buf[idx] = (addr >> 8) & 0xff;
        idx++;
        buf[idx] = addr & 0xff;
        idx++;
    } else {
        buf[idx] = addr & 0xff;
        idx++;
    }

    if (ov12870_data_byte == 2) {
        buf[idx] = (data >> 8) & 0xff;
        idx++;
        buf[idx] = data & 0xff;
        idx++;
    } else {
        buf[idx] = data & 0xff;
        idx++;
    }

    ret = write(g_fd[ViPipe], buf, (ov12870_addr_byte + ov12870_data_byte));

    if (ret < 0) {
        ISP_ERR_TRACE("I2C_WRITE DATA error!\n");
        return HI_FAILURE;
    }

    return HI_SUCCESS;
}

void ov12870_standby(VI_PIPE ViPipe)
{
    ov12870_write_register(ViPipe, 0x100, 0x00);
    g_bStandby[ViPipe] = HI_TRUE;
    return;
}

void ov12870_restart(VI_PIPE ViPipe)
{
    ov12870_write_register(ViPipe, 0x100, 0x01);
    g_bStandby[ViPipe] = HI_FALSE;
    return;
}

void ov12870_mirror_flip(VI_PIPE ViPipe, ISP_SNS_MIRRORFLIP_TYPE_E eSnsMirrorFlip)
{
    HI_U8 u8ImgMode;
    u8ImgMode   = g_pastOv12870[ViPipe]->u8ImgMode;
    HI_U8 u8BitMask = 0xFF;
    if ((u8ImgMode == OV12870_8M_30FPS_10BIT_LINEAR_MODE) || (u8ImgMode == OV12870_12M_30FPS_10BIT_LINEAR_MODE)) {
        u8BitMask = 0xFE;
    } else {
        u8BitMask = 0xFF;
    }
    switch (eSnsMirrorFlip) {
        default:
        case ISP_SNS_NORMAL:
            ov12870_write_register(ViPipe, 0x3821, (0x5&u8BitMask));
            ov12870_write_register(ViPipe, 0x3820, 0x0);
            break;

        case ISP_SNS_MIRROR:
            ov12870_write_register(ViPipe, 0x3821, (0x1&u8BitMask));
            ov12870_write_register(ViPipe, 0x3820, 0x0);
            break;

        case ISP_SNS_FLIP:
            ov12870_write_register(ViPipe, 0x3821, (0x5&u8BitMask));
            ov12870_write_register(ViPipe, 0x3820, 0x4);
            break;

        case ISP_SNS_MIRROR_FLIP:
            ov12870_write_register(ViPipe, 0x3821, (0x1&u8BitMask));
            ov12870_write_register(ViPipe, 0x3820, 0x4);
            break;
    }

    return;
}

